package model;

public class Userposjava {

	private Long ind;
	private String nome;
	private String email;

	public Long getInd() {
		return ind;
	}

	public void setInd(Long ind) {
		this.ind = ind;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

}
